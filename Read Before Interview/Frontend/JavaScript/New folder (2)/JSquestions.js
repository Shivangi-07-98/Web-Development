// Q1. What is javascript? what is the role of javascript engine?
// Ans. Javascript is a programming language that is used for converting static web pages to interactive and dynamic web pages.
// A javascript engine is a program present in web browsers that executes javascript code.

// Q2. What are variables?
// Ans. Variables are used to store data.

// Q3. What is DOM? What is the difference between html and dom?
// Ans. The DOM document object model represents the web page as a tree like structure that allows javascript to dynamically access and manipulate the content and structure of a web page.

// Q4. What are selectors in js?
// Ans. DOM selector methods 
// getElementById() 
// getElementsByClassName() 
// getElementByTagName() 
// querySelector() 
// querySelectorAll() 
// Selectors in JS help to get specific elements from DOM based on Ids, class names, tag names.

// Q5. What are data types in JS?
// Ans. A data type determines the type of variable.
// Types of data types- Primitive -> Numbers, Strings, Booleans, Undefined, Null
// Non-Primitive -> Object, Array, Function, Date, RegExp

// Q6. What is a loop? What are the types of loops in javascript?
// Ans. A loop is a programming way to run a piece of code repeatedly until a certain condition is met.
// Javascript loops -> for, while, do-while, for...of, for...in

// Q7. What are Arrays in javascript. 
// Ans. An array is a data type that allows you to store multiple values in a single variable.

// Q8. What are objects in javascript? 
// Ans. An object is a data type that allows you to store key-value pairs.

// Q9. What is hoisting in javascript?
// Ans. Hoisting is a javascript behaviour where functions and variable declarations are moved to the top of their respective scopes during the compilation phase.

// Q10. What is the use of typeof operator?
// Ans. typeof operator is used to determine the type of each variable.

// Q11. What is type coercion in JS?
// Ans. Type coercion is the automatic conversion of values from one data type to another during certain operations or comparisons.

